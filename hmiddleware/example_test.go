func ExampleCORS() {
	r := chi.NewRouter()
	r.Use(CORS)

	// OR

	var h http.Handler

	//nolint: gosec
	if err := http.ListenAndServe(":"+os.Getenv("PORT"), CORS(h)); err != nil {
		log.Fatal(err)
	}
}

